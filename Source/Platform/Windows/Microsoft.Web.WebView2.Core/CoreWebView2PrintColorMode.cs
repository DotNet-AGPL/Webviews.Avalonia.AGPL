namespace Microsoft.Web.WebView2.Core;

/// <summary>Specifies the color mode for a print.</summary>
public enum CoreWebView2PrintColorMode
{
	/// <summary>The default color mode for a printer.</summary>
	Default,
	/// <summary>Indicate that the printed output will be in color.</summary>
	Color,
	/// <summary>Indicate that the printed output will be in shades of gray.</summary>
	Grayscale
}
