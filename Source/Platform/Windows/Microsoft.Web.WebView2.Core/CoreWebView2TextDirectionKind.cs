namespace Microsoft.Web.WebView2.Core;

/// <summary>Indicates the text direction of the notification.</summary>
public enum CoreWebView2TextDirectionKind
{
	/// <summary>Indicates that the notification text direction adopts the browser's language setting behavior.</summary>
	Default,
	/// <summary>Indicates that the notification text is left-to-right.</summary>
	LeftToRight,
	/// <summary>Indicates that the notification text is right-to-left.</summary>
	RightToLeft
}
